'''
8. Faça um programa em Python utilizando a biblioteca Turtle, que desenhe a casa
como mostra a figura abaixo. Desenha um triângulo equilátero sobre um
quadrado com o mesmo comprimento de lado. A tartaruga deve voltar para o seu
local de origem de acordo como mostra a imagem.
'''
import turtle
turtle.shape("turtle")
turtle.forward(150)
turtle.left(90)
turtle.forward(150)
turtle.left(90)
turtle.forward(150)
turtle.right(120)
turtle.forward(150)
turtle.right(120)
turtle.forward(150)
turtle.right(120)
turtle.forward(150)
turtle.left(90)
turtle.forward(150)
turtle.done()